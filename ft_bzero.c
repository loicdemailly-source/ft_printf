#include "ft_printf.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (i < n && n != 0)
	{
		str[i] = '\0';
		i++;
	}
}
